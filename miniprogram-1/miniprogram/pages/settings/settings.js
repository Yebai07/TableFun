  logout() {
    wx.showModal({
      title: '退出登录',
      content: '确定要退出登录吗？',
      success: (res) => {
        if (res.confirm) {
          // 清除本地存储的用户信息
          wx.removeStorageSync('userInfo');
          
          // 执行退出登录操作
          wx.showToast({
            title: '已退出登录',
            icon: 'success'
          });
          
          // 返回到个人中心页面（个人中心会检测未注册状态并显示注册入口）
          setTimeout(() => {
            wx.navigateBack({
              delta: 1
            });
          }, 1000);
        }
      }
    });
  },

